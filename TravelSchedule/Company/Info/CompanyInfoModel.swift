
/// Модель детальной информации о перевозчике.
///
/// Используется для отображения данных на экране
/// с подробной информацией о компании.
struct CompanyInfoModel: Decodable, Sendable {
    
    /// Название изображения большого логотипа компании.
    /// Используется для отображения логотипа в UI.
    let bigLogoName: String
    
    /// Полное официальное название компании.
    let fullCompanyName: String
    
    /// Электронная почта перевозчика.
    let email: String
    
    /// Контактный телефон перевозчика.
    let phone: String
    
    /// Уникальный код компании (например, код перевозчика в системе).
    let code: Int
}
