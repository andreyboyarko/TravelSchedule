

import Foundation

/// Модель, описывающая один вариант маршрута/перевозчика в списке.
///
/// Используется для отображения ячейки в списке (логотип, времена, дата, пересадка),
/// а также содержит данные для экрана деталей перевозчика.
struct CompanyModel: Identifiable, Decodable, Sendable {

    /// Уникальный идентификатор для SwiftUI-списков (`List/ForEach`).
    /// ⚠️ Важно: при `Decodable` это поле **не декодируется** из JSON автоматически,
    /// а будет создаваться новым при каждой инициализации модели.
    /// Если у API есть стабильный id (например `code`/`uid`) — лучше использовать его.
    var id = UUID()

    /// Название перевозчика/компании (например, "РЖД", "Аэрофлот").
    let companyName: String

    /// URL-строка для логотипа компании (используется в `AsyncImage`).
    let image: String

    /// Время отправления (как строка для UI, например "10:45").
    let timeToStart: String

    /// Время прибытия (как строка для UI, например "13:20").
    let timeToOver: String

    /// Общая длительность пути (например "2ч 35м").
    let allTimePath: String

    /// Дата рейса/маршрута (например "17 дек" или "2025-12-17").
    let date: String

    /// Флаг: есть ли пересадка на маршруте.
    let needSwapStation: Bool

    /// Название/станция пересадки (если есть).
    let swapStation: String?

    /// Время суток для фильтрации (утро/день/вечер/ночь).
    let timeOfDay: TimeOfDay

    /// Детальная информация о перевозчике для экрана CompanyInfo.
    let detailInfo: CompanyInfoModel
}
